import UIKit

struct ButtonLayout {
    
    struct LargeButton {
        static let width: CGFloat = 175
        static let height: CGFloat = 75
    }
    
    struct Flat {
        static let width: CGFloat = 120
        static let height: CGFloat = 44
    }
    
    struct Raised {
        static let width: CGFloat = 150
        static let height: CGFloat = 44
    }
    
    struct Fab {
        static let diameter: CGFloat = 48
    }
    
    struct Icon {
        static let width: CGFloat = 120
        static let height: CGFloat = 48
    }
}
